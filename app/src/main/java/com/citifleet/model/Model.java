public class Model {
    private int id;
    private String name;
    private int make;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMake() {
        return make;
    }

    public void setMake(int make) {
        this.make = make;
    }
}
